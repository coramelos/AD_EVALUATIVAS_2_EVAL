    /**
     * Accion para descargar todos los pedidos de la base de datos
     */
    public void loadPedidos(){
    
        ArrayList<Pedido>pedidos = this.pedidoService.getAll();
        for(Pedido pedido :pedidos){
            
            this.txtPedidos.append(pedido.toString() + "\n" );
        }
    }

    

   
